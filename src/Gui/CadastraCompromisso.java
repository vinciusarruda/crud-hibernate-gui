    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        ControllerCompromisso compromisso= new ControllerCompromisso();
        try {
            if(dia.getText().isEmpty()||hora.getText().isEmpty())
            JOptionPane.showMessageDialog(null,"Preencha todos os campos");
            else
            if(codigo.getText().isEmpty()){
                compromisso.salvar(dia.getText(), hora.getText(), assunto.getText(), nome);
                dia.setText("");
                hora.setText("");
                assunto.setText("");

            }else{
                compromisso.salvar(Integer.parseInt(codigo.getText()),dia.getText(), hora.getText(), assunto.getText());
                new ConsultaCompromissos(nome).setVisible(true);
                dispose();
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,"Ops, algo deu errado: "+ ex);
        }
    }//GEN-LAST:event_salvarActionPerformed

